#Implementar un programa en Python que pida al usuario que ingrese dos números y muestre la suma, 
#resta, división y multiplicación de ambos.

numero1 = input("Ingrese primer numero")
numero2 = input("Ingrese segundo numero")

suma = numero2 + numero1

resta = numero2 - numero1

multiplicacion = numero1 * numero2

division = numero1 / numero2

print(f"la suma es {suma}, la resta es {resta} , la multiplicacion es {multiplicacion}, la division es {division}")